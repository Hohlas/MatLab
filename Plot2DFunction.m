%% Параметрические чертежи - окружность с радиусом 1, центром в точке (0,0)
x=cos(2*pi*T); 
y=sin(2*pi*T);
T = 0 : 0.01 : 1; 
plot(x,y) % чертит график на основе числовых данных, 
axis square % одинаковую шкалу на обеих осях; без этой команды окружность приняла бы вид эллипса.
title('plot:  x=cos(2\piT), y=sin(2\piT)')% "\pi" перед названием греческой буквы знак обратной косой черты для ее отображения
% либо через ezplot
figure % create figure graphics object
ezplot('cos(T)','sin(T)',[0 2*pi]) % отображает графики функций, задаваемых рядом или символьным вводом. 
axis square
title('ezplot:  x=cos(T), y=sin(T), T=0..2\pi') 

%% Контурные и неявные чертежи функции с двумя переменными x^2 + y^2
% кривые  уровня функции, то есть множества точек в плоскости х-у, где функция принимает постоянное значение. 
[X Y] = meshgrid(-3:0.1:3, -3:0.1:3); % создает сетку из точек в прямоугольной области с заданными интервалами. 
contour(X, Y, X.^2 + Y.^2); axis square % Эта сетка используется командой contour для создания контурного чертежа в заданной области. 
% можно определить множество уровней путем включения дополнительного векторного аргумента в команду contour: 
figure
