axis square 
title('окружность x^2 + y^2') 

%% Распределение Максвелла для молекул водорода для двух значений температур
clear %Remove items from workspace 
close all % Remove all figures
figure % create figure graphics object
help graph2d 
m=3.32e-27; k=1.38e-23; T1=300; T2=500;% Распределение Максвела молекул по скоростям 
n=3000; step=2;% Задаются количество шагов и шаг по оси ординат
v(1)=0; f1(1)=0; f2(1)=0;% Начальные значения скорости и функции распределения    
for i=2:n % В цикле создаются массивы скорости и функции распределения
    v(i)=v(i-1)+step;
    f1(i)=4/sqrt(pi)*(m/(2*k*T1))^1.5*v(i)^2*exp(-m*v(i)^2/(2*k*T1));
    f2(i)=4/sqrt(pi)*(m/(2*k*T2))^1.5*v(i)^2*exp(-m*v(i)^2/(2*k*T2));
    end
plot(v,f1,v,f2) % чертит график на основе числовых данных,  
grid
set(gca,'FontName','Arial Cyr');
title('Распределение Максвелла','FontSize',14, 'FontName','Helvetica', 'FontWeight','bold'); 
%disp ('расстановка надписей') % чтобы отобразить сообщение перед паузой.
xlabel('Скорость'); 
ylabel('Плотность вероятности');
text(2000,5E-4,'максимум функции '); %, 'FontSize',12
legend

